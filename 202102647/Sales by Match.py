def sockMerchant(n, ar):
    # Write your code here
    ar.sort()
    count = 0
    stack = [ar[0]]
    for i in range (1,len(ar)):
        stack.push(ar[i])
        if len(stack)>1:
            a = stack.pop()
            b = stack.pop()
            if a==b:
                count+=1
            else:
                stack.push(a)
    return count

